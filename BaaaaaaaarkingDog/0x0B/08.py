import sys
input = sys.stdin.readline
N = int(input())
board = [[' ' for _ in range(N)] for _ in range(N)]

def solution(x, y, N):
    count = 0
    for dx in range(3):
        for dy in range(3):
            count += 1
            if count == 5: continue # 9번 반복 중에 5번째는 공백임
            if N == 3: board[x + dx][y + dy] = '*'
            else: solution(x + dx * N // 3, y + dy * N // 3, N // 3) # 위치 이동

solution(0, 0, N)

for i in board:
    print("".join(i))
